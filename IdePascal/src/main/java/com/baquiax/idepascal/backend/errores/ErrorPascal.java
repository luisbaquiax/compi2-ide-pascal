package com.baquiax.idepascal.backend.errores;

/**
 *
 * @author Luis
 */
public class ErrorPascal {

    private String type;
    private String description;
    private int row;
    private int col;

    public ErrorPascal(String type, String description, int row, int col) {
        this.type = type;
        this.description = description;
        this.row = row;
        this.col = col;
    }

    /**
     * @return the type
     */
    public String getType() {
        return type;
    }

    /**
     * @param type the type to set
     */
    public void setType(String type) {
        this.type = type;
    }

    /**
     * @return the description
     */
    public String getDescription() {
        return description;
    }

    /**
     * @param description the description to set
     */
    public void setDescription(String description) {
        this.description = description;
    }

    /**
     * @return the row
     */
    public int getRow() {
        return row;
    }

    /**
     * @param row the row to set
     */
    public void setRow(int row) {
        this.row = row;
    }

    /**
     * @return the col
     */
    public int getCol() {
        return col;
    }

    /**
     * @param col the col to set
     */
    public void setCol(int col) {
        this.col = col;
    }

}
